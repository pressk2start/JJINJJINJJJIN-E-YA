def count_match(data, condition):
    return sum(1 for d in data if condition(d))

def analyze(name, fail_cond, succ_cond):
    f = count_match(fail_data, fail_cond)
    s = count_match(success_data, succ_cond)
    total = f + s
    if total > 0:
        rate = s / total * 100
        print(f"{name}:")
        print(f"  실패 {f}건, 성공 {s}건 → 성공률 {rate:.1f}%")
        return rate, f, s
    return None, 0, 0

